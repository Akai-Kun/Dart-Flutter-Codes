//Dart data types:-
//1. Integer
//2. String
//3. Boolean
//4. List (more like arrays)
//5. Maps (more like dictionaries)
//6. Double (float)

void main() {
  //1. Integer
  int age = 20;
  print(age);
  //2. String
  String name = "John";
  print(name);
  //3. Boolean
  bool isAlive = true;
  print(isAlive);
  //4. List
  List<String> names = ["John", "Jane", "Jack"];
  print(names);
  //5. Maps
  Map<String, int> ages = {"John": 20, "Jane": 21, "Jack": 22};
  print(ages);
  //6. Double
  double height = 1.75;
  print(height);
}
